class Point:
    '''A class to model a point.
    Attributes:
        x and y '''
    def __init__(self, x, y):
        self.x = x
        self.y = y

class Circle:
    '''A class to model a circle.
    Attributes:
        x, y, radius'''
    def __init__(self, center, radius):
        self.center = center
        self.radius = radius


